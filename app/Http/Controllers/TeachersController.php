    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $teacher = new Teacher;
        $teacher->nuptk = $request->nuptk;
        $teacher->nama = $request->nama;
        $teacher->ttl = $request->ttl;
        $teacher->alamat = $request->alamat;
        $teacher->tlp = $request->tlp;
        $teacher->email = $request->email;
        $teacher->jabatan = $request->jabatan;
        $teacher->pendidikan = $request->pendidikan;
        $teacher->gambar = $request->gambar;
        $teacher->fb = $request->fb;
        $teacher->ig = $request->ig;
        $teacher->save();
        return redirect('/teachers');
    }

